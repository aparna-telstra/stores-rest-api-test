name = "Anya"
#A variable can begin with a lowercase or an underscore.
puts "#{name}"

first_name = "Leia"
lastName = "Mahesh"
#lastName - this way of writing variables is known as camelcasing.
puts "Anya's sister is #{first_name} #{lastName}."

$salary = 1000
#starting a variable with a $ makes it a global variables

puts "#{$salary} is a global variable"

PI = 3.14159
#Writing a variable in capital makes it a CONSTANT.
puts "#{PI} is a constant"
